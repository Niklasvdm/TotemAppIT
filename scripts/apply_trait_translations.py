def main():
    with open(DATA_FILE, encoding="utf-8") as f:
        animals = json.load(f)

    updated = 0
    unmapped = set()
    for a in animals:
        if not a.get("traits_it") and a.get("traits_nl"):
            translated = []
            for t in a["traits_nl"]:
                it = NL_TO_IT.get(t)
                if it:
                    translated.append(it)
                else:
                    unmapped.add(t)
                    translated.append(t)  # fallback: keep Dutch
            a["traits_it"] = translated
            updated += 1

    with open(DATA_FILE, "w", encoding="utf-8") as f:
        json.dump(animals, f, ensure_ascii=False, indent=2)

    print(f"Updated traits_it for {updated} animals.")
    if unmapped:
        print(f"Unmapped traits (kept as-is): {sorted(unmapped)}")

if __name__ == "__main__":
    main()
